    def decrypt_privKey_file(self):
        with open('user.bin', 'rb') as user_file:
            username = user_file.read().decode('UTF-8')
        user_file.close()
        key = self.create_symmetric_key(username)
        with open(username + '_priv_encrypted.pem', 'rb') as priv_dec_key:
            cipher_pub_key = priv_dec_key.read()
        priv_dec_key.close()
        fernet = Fernet(key)
        decrypted = fernet.decrypt(cipher_pub_key)
        return decrypted.decode('UTF-8')
